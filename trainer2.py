def play_game(state, net, move_to_here, turn='max', k = 5):
    if move_to_here != None:
        if terminal_test(state, k, move_to_here):
            if turn == 'max':
                return 0
            else:
                return 1
    
    moves = generate_successor_moves(state, turn)
    if len(moves) == 0:
        return 0.4
    
    piece_type = (-1 if turn == 'min' else 1)

    best_move = moves[0]
    best_val = piece_type*-2

    for move in moves:
        state[move[0]][move[1]] = piece_type
        unrolled = np.reshape(state, state.shape[0]*state.shape[1])
        val = piece_type*net.feedforward(unrolled)
        if val > best_val:
            best_move = move
            best_val = val
        state[move[0]][move[1]] = 0

    state[best_move[0]][best_move[1]] = piece_type
    next_turn = ('min' if turn == 'max' else 'max')

    res = play_game(state, net, best_move, next_turn, k)

    mod_res = res
